// len64 returns the minimum number of bits required to represent x; the result is 0 for x == 0.
func len64(x uint64) (n uint) {
	if x >= 1<<32 {
		x >>= 32
		n = 32
	}
	if x >= 1<<16 {
		x >>= 16
		n += 16
	}
	if x >= 1<<8 {
		x >>= 8
		n += 8
	}
	return n + uint(len8tab[x])
}

func leadingZeroes64(v uint64) uint {
	return 64 - len64(v)
}
